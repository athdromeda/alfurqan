import { useState, useEffect, useRef } from "react";
import axios from "axios";
import Head from "next/head";
import styles from "../styles/Home.module.css";

const Ayah = ({ text }) => {
  return (
    <section>
      <p className={styles.ayahtext}>{text} €ù</p>
      <hr />
    </section>
  );
};

const SurahTitle = ({ data }) => {
  return (
    <section>
      <h3>{data.name}</h3>
      <h4>{data.englishName + " " + data.englishNameTranslation}</h4>
    </section>
  );
};

const Hero = ({ handleForm, handleQuery }) => {
  const refQuery = useRef();

  return (
    <section className={styles.hero}>
      <h3>Welcome to</h3>
      <h1>al Furqan</h1>

      <form onSubmit={handleForm}>
        <input
          ref={refQuery}
          type="number"
          placeholder="Masukkan nomor surah di sini!"
        />
        <button type="submit" onClick={()=>handleQuery(refQuery.current.value)}>Cari</button>
      </form>
    </section>
  );
};

const baseURL = "http://api.alquran.cloud/v1/surah/";
export default function Home() {
  const [surah, setSurah] = useState(null);
  const [spinner, setSpinner] = useState(true);
  const [query, setQuery] = useState('');

  useEffect(() => {
    axios.get(baseURL + "1").then((res) => {
      setSurah(res.data);
      setSpinner(false);
    });
  }, []);

  const handleForm = (e) => {
    e.preventDefault();
    axios.get(baseURL + query).then((res) => {
      setSurah(res.data);
    });
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Alfurqan - Read your Quran</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <Hero handleForm={handleForm} handleQuery={setQuery}/>

        {spinner ? (
          <h1>Loading...</h1>
        ) : (
          <>
            <SurahTitle data={surah.data} />
            {surah.data.ayahs.map((ayah) => (
              <Ayah text={ayah.text} />
            ))}
          </>
        )}
      </main>
    </div>
  );
}
